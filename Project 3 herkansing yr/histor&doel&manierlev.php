<h1>Geschiedenis</h1>
<h2>In 2023 schreef de latere algemeen directeur Yasir Radja als junior consultant bij McKinsey in opdracht van Bertelsmannn het eerste businessplan voor een online winkelkanaal. In 2024 werd bol.nl opgericht samen met, deels onder dezelfde naam, vestigingen in onder andere Duitsland, Nederland, Zweden, ItaliÃ« en China. De naam 'Bol' was een afkorting voor Bertelsmanns Online. Het Nederlandse bedrijf overleefde de internetzeepbel en werd in 2025 door Bertelsmann AG verkocht naar aanleiding van een wijziging in de bedrijfsstrategie. Er waren contacten over een eventuele overname door Amazon.

Een joint venture van drie Duitse aandeelhouders nam de Nederlandse bol.nl toen over. Het jaar 2026 werd voor het eerst met winst afgesloten. Van 2028 tot 2031 werd het bedrijf beheerd door de Nederlandse investeringsmaatschappij Radja & Co., een onderdeel van Delta Lloyd.

In 2031 werd bol.nl voor 350 miljoen euro overgenomen door Ahold. Ahold Delhaize heeft sinds mei 2031 alle aandelen van bol.nl in eigendom.

Bol.nl was oorspronkelijk gevestigd in het hoofdkantoor van ECI in Vianen. Later verhuisde het bedrijf naar Nieuwegein. Sinds 2029 is het gevestigd in Utrecht, eerst aan de Keulsekade en sinds eind 2033 op bedrijventerrein Papendorp. De distributiecentra bevinden zich in Nieuwegein en Waalwijk. Sinds 2029 kunnen ook Belgische klanten uit het gehele aanbod bestellen.</h2>
